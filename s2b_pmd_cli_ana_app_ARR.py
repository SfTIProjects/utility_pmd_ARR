# Run PMD static code analysis
###################################################################

def run_pmd_analysis():
        
    #e.g. my-javacodeanalysis-app0
    app_name_full = '{}{}'.format(java_app, array_id)
    
    #e.g. my-javacodeanalysis-app0
    app_name_full_dest_path = app_name_full
    
    
    if use_comm_root_dir == True and use_parent_dest_path == True:
        #e.g., ../my_codesnippet_analysis/pmd/mvn_apps/my-javacodeanalysis-app0
        app_name_full_dest_path = '{}/{}/{}'.format(comm_root_dir, parent_dest_path, app_name_full)
    elif use_comm_root_dir == True:
        #e.g., ../my_codesnippet_analysis/pmd/mvn_apps/my-javacodeanalysis-app0
        app_name_full_dest_path = '{}/{}'.format(comm_root_dir, app_name_full)
    elif use_parent_dest_path == True:
        app_name_full_dest_path = '{}/{}'.format(parent_dest_path, app_name_full)
        
    #print('@@ {}'.format(app_name_full_parent_dest_path))
    
    #e.g., ../my_codesnippet_analysis/pmd/mvn_apps/my-javacodeanalysis-app0/src/main/java
    app_name_full_child_dest_path = '{}/{}'.format(app_name_full_dest_path, child_dest_path)
    #e.g., ../my_codesnippet_analysis/pmd/mvn_apps/my-javacodeanalysis-app0/src/main/java/myjavacodeanalysis
    app_name_full_child_dest_path_pkg = '{}/{}'.format(app_name_full_child_dest_path, java_pkg)
    
    log_file_full = log_file
    comp_java_files_full = comp_java_files
    error_java_files_full = error_java_files
    xml_rep_files_full = xml_rep_files
   
    
    if use_comm_root_dir == True:
        #e.g. ../my_codesnippet_analysis/pmd/pmd_logs.txt
        log_file_full = '{}/{}'.format(comm_root_dir, log_file)
        #e.g., ../my_codesnippet_analysis/pmd/errorcodeanalysis
        error_java_files_full = '{}/{}'.format(comm_root_dir, error_java_files)
        
        if use_comp_java_files == True:
            #e.g., ../my_codesnippet_analysis/pmd/completecodeanalysis
            comp_java_files_full = '{}/{}'.format(comm_root_dir, comp_java_files)
        
        if use_xml_rep_files == True:
            #e.g., ../my_codesnippet_analysis/pmd/xmlanalysisreports
            xml_rep_files_full = '{}/{}'.format(comm_root_dir, xml_rep_files)
        
        
        
        
    error = True
    count = 0
        
    while error:
        #e.g., pmd_cmd = 'cd ../my_codesnippet_analysis/pmd/mvn_apps/my-javacodeanalysis-app0/pmd-bin-6.39.0/bin; chmod u+x; ./run.sh pmd -d ../../src/main/java/myjavacodeanalysis -f xml -R ../../pmdrules.xml -r ../../pmd.xml
        pmd_cli_cmd = 'cd {}/{}/bin; chmod u+x run.sh; ./run.sh pmd -d ../../{}/{} -f xml -R ../../{} -r ../../pmd.xml'.format(app_name_full_dest_path, pmd_exe_folder, child_dest_path, java_pkg, pmd_xml_rules)
        cmd1 = sp.run(
            pmd_cli_cmd, # command
            capture_output=True,
            text=True,
            shell=True
        )
        
        # capture the output 
        log_msg = cmd1.stdout
        #log_msg = 'ERROR'

        # Perform some text processing
        # 1. capture the error msg and save it into a log file

        # Open a file with access mode 'a'
        #with open("pmd_logs.txt", "a") as file_object:
        with open(log_file_full, "a") as file_object:
            
            # Append log_msg at the end of file
            file_object.write(log_msg)
            
        # If there is error in the log file
        if 'ERROR' in log_msg: 

            error = True
            # 2. retrieve the erroneous file
            pattern = re.compile(r'Code_\d+_\d+_\d+_\d+\.java')
            #pattern = re.compile(r'Code_\d+_\d+_\d+_\d+\.{}'.format(src_type))
            #filename_match = pattern.findall(log_msg)[0]
            filename_match = pattern.findall(log_msg)
            #filename_match ='Code_52604169_52604034_2681_0.java'
            if len(filename_match) > 0:
                filename_match = filename_match[0]
                # print(filename_matche)

                # 3. Move the erroneous file to another folder 'errorcodeanalysis'
                #e.g., mv_erroneous_file_cmd = 'mv ../my_codesnippet_analysis/pmd/mvn_apps/my-javacodeanalysis-app0/src/main/java/myjavacodeanalysis/Code_1_1_1_1.java' ../my_codesnippet_analysis/pmd/errorcodeanalysis'
                mv_erroneous_file_cmd = 'mv {}/{} {}'.format(app_name_full_child_dest_path_pkg, filename_match, error_java_files_full)
                #print("@@MOVE@@ {}".format(mv_erroneous_file_cmd))
                cmd2 = sp.run(
                    mv_erroneous_file_cmd, # command
                    capture_output=True,
                    text=True,
                    shell=True
                )
                   
        else: # else If there is error when carring out the pmd
            error = False

        count = count + 1
        # 4. re-run the comand until all the error files have been eliminated

      
    #2. Move the java files of thoes that completed into a completecodeanalysis
    # e.g., mv_complete_file_cmd = 'mv ../my_codesnippet_analysis/pmd/mvn_apps/my-javacodeanalysis-app0/src/main/java/myjavacodeanalysis/*.java ../my_codesnippet_analysis/pmd/completecodeanalysis'
    if move_comp_files == True:
        mv_complete_file_cmd = 'mv {}/*.{} {}'.format(app_name_full_child_dest_path_pkg, src_type, comp_java_files_full)
        cmd3 = sp.run(
            mv_complete_file_cmd, # command
            capture_output=True,
            text=True,
            shell=True
        )
      
        
    if use_xml_rep_files == True:
        # Copy and rename pmd.xml at the same time 
        # e.g., cd ../my_codesnippet_analysis/pmd/mvn_apps/my-javacodeanalysis-app0; cp pmd.xml pmd0.xml
        cd_mv_xml_rep_frm_cmd = 'cd {}; cp pmd.xml pmd{}.xml'.format(app_name_full_dest_path, array_id)
        cmd4 = sp.run(
            cd_mv_xml_rep_frm_cmd, # command
            capture_output=True,
            text=True,
            shell=True
        )
        
        #move the renamed pmd.xml to another location
        # e.g., mv ../my_codesnippet_analysis/pmd/mvn_apps/my-javacodeanalysis-app0/*pmd0.xml ../my_codesnippet_analysis/pmd/xmlanalysisreports
        mv_xml_rep_frm_cmd = 'mv {}/pmd{}.xml {}'.format(app_name_full_dest_path, array_id, xml_rep_files_full)
        cmd5 = sp.run(
            mv_xml_rep_frm_cmd, # command
            capture_output=True,
            text=True,
            shell=True
        )
    
    

    
# Run the PMD Analysis
run_pmd_analysis()

print('Pmd Analysis Complete Rank {} -:)'.format(array_id))
